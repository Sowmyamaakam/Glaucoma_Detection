def generate_saliency_map(img_tensor, model):
    """Generate saliency map"""
    model.eval()
    img_tensor.requires_grad = True

    seg_out, cls_out = model(img_tensor)
    pred_class = torch.argmax(cls_out, dim=1)

    model.zero_grad()
    cls_out[0, pred_class].backward()

    saliency = img_tensor.grad.data.abs()
    saliency = torch.max(saliency, dim=1)[0]
    saliency = saliency.squeeze().cpu().numpy()
    saliency = (saliency - saliency.min()) / (saliency.max() - saliency.min() + 1e-8)

    return saliency

