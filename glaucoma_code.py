class CachedGlaucomaDataset(Dataset):
    def __init__(self, csv_file, split='train', augment=False, use_cache=True):
        self.df = pd.read_csv(csv_file, encoding='latin1')
        self.df = self.df[self.df['split'] == split].reset_index(drop=True)
        self.augment = augment and split == 'train'
        self.img_size = 512
        self.use_cache = use_cache
        self.cache = {}

        if self.use_cache:
            print(f"\n{'='*70}")
            print(f"CACHING {split.upper()} DATASET IN MEMORY")
            print(f"{'='*70}")
            for idx in tqdm(range(len(self.df)), desc=f"Caching {split}"):
                row = self.df.iloc[idx]
                image = Image.open(row['image_path']).convert("RGB")
                mask = Image.open(row['mask_path']).convert("L")
                self.cache[idx] = {
                    'image': image.copy(),
                    'mask': mask.copy(),
                    'label': row['label']
                }
            print(f"✓ {len(self.cache)} images cached!\n")

    def __len__(self):
        return len(self.df)

    def __getitem__(self, idx):
        if self.use_cache and idx in self.cache:
            image = self.cache[idx]['image'].copy()
            mask = self.cache[idx]['mask'].copy()
            label = torch.tensor(self.cache[idx]['label'], dtype=torch.long)
        else:
            row = self.df.iloc[idx]
            image = Image.open(row['image_path']).convert("RGB")
            mask = Image.open(row['mask_path']).convert("L")
            label = torch.tensor(row['label'], dtype=torch.long)

        # CLAHE preprocessing
        img_cv = cv2.cvtColor(np.array(image), cv2.COLOR_RGB2LAB)
        l, a, b = cv2.split(img_cv)
        clahe = cv2.createCLAHE(clipLimit=2.0, tileGridSize=(8, 8))
        l_clahe = clahe.apply(l)
        img_clahe = cv2.merge((l_clahe, a, b))
        image = Image.fromarray(cv2.cvtColor(img_clahe, cv2.COLOR_LAB2RGB))

        if self.augment and random.random() > 0.5:
            image = T.ColorJitter(brightness=0.2, contrast=0.2)(image)

        transform_img = T.Compose([
            T.Resize((self.img_size, self.img_size)),
            T.ToTensor(),
            T.Normalize([0.485, 0.456, 0.406], [0.229, 0.224, 0.225])
        ])
        transform_mask = T.Compose([
            T.Resize((self.img_size, self.img_size), interpolation=T.InterpolationMode.NEAREST),
            T.ToTensor()
        ])

        return transform_img(image), transform_mask(mask), label

