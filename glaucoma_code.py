# Capsule Network Layers
# =====================================================
class PrimaryCaps(nn.Module):
    def __init__(self, input_channels, num_capsules=8, dim_capsule=16, kernel_size=3, stride=2):
        super().__init__()
        self.conv = nn.Conv2d(input_channels, num_capsules * dim_capsule,
                              kernel_size=kernel_size, stride=stride, padding=1)
        self.num_capsules = num_capsules
        self.dim_capsule = dim_capsule

    def squash(self, x, eps=1e-7):
        norm = torch.norm(x, dim=2, keepdim=True)
        scale = (norm ** 2) / (1 + norm ** 2)
        return scale * x / (norm + eps)

    def forward(self, x):
        B = x.size(0)
        u = self.conv(x)
        u = u.view(B, self.num_capsules, self.dim_capsule, -1)
        u = u.permute(0, 1, 3, 2).contiguous()
        u = u.view(B, -1, self.dim_capsule)
        return self.squash(u)

class DigitCaps(nn.Module):
    def __init__(self, dim_caps_in, num_caps_out=2, dim_caps_out=16, num_routing=3):
        super().__init__()
        self.num_caps_out = num_caps_out
        self.dim_caps_out = dim_caps_out
        self.num_routing = num_routing
        self.W = None

    def squash(self, x, eps=1e-7):
        norm = torch.norm(x, dim=-1, keepdim=True)
        scale = (norm ** 2) / (1 + norm ** 2)
        return scale * x / (norm + eps)

    def forward(self, x):
        B, num_caps_in, dim_caps_in = x.size()
        if self.W is None or self.W.size(1) != num_caps_in:
            self.W = nn.Parameter(0.01 * torch.randn(1, num_caps_in, self.num_caps_out,
                                                     self.dim_caps_out, dim_caps_in, device=x.device))
        x = x.unsqueeze(2).unsqueeze(4)
        u_hat = torch.matmul(self.W, x).squeeze(4)
        b_ij = torch.zeros(1, num_caps_in, self.num_caps_out, 1, device=x.device)

        for r in range(self.num_routing):
            c_ij = F.softmax(b_ij, dim=2)
            s_j = (c_ij * u_hat).sum(dim=1, keepdim=True)
            v_j = self.squash(s_j)
            if r < self.num_routing - 1:
                b_ij = b_ij + (u_hat * v_j).sum(dim=-1, keepdim=True)
        return v_j.squeeze(1)

