def generate_gradcam(img_tensor, model, target_layer):
    """Generate Grad-CAM visualization"""
    model.eval()

    # Forward pass
    activations = []
    gradients = []

    def forward_hook(module, input, output):
        activations.append(output)

    def backward_hook(module, grad_input, grad_output):
        gradients.append(grad_output[0])

    handle_forward = target_layer.register_forward_hook(forward_hook)
    handle_backward = target_layer.register_full_backward_hook(backward_hook)

    seg_out, cls_out = model(img_tensor)
    pred_class = torch.argmax(cls_out, dim=1)

    model.zero_grad()
    cls_out[0, pred_class].backward()

    activation = activations[0].detach()
    gradient = gradients[0].detach()

    weights = torch.mean(gradient, dim=[2, 3], keepdim=True)
    cam = torch.sum(weights * activation, dim=1, keepdim=True)
    cam = F.relu(cam)
    cam = F.interpolate(cam, size=img_tensor.shape[2:], mode='bilinear', align_corners=False)
    cam = cam.squeeze().cpu().numpy()
    cam = (cam - cam.min()) / (cam.max() - cam.min() + 1e-8)

    handle_forward.remove()
    handle_backward.remove()

    return cam

