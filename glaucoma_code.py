# HACapsNet Model
# =====================================================
class HACapsNet(nn.Module):
    def __init__(self, num_classes=2):
        super().__init__()
        backbone = models.resnet18(pretrained=True)
        self.encoder = nn.Sequential(*list(backbone.children())[:-2])
        self.feature_dim = 512

        self.decoder = nn.Sequential(
            nn.ConvTranspose2d(self.feature_dim, 256, 2, stride=2),
            nn.ReLU(),
            nn.ConvTranspose2d(256, 128, 2, stride=2),
            nn.ReLU(),
            nn.ConvTranspose2d(128, 1, 1)
        )

        self.primary_caps = PrimaryCaps(input_channels=self.feature_dim,
                                        num_capsules=8, dim_capsule=16)
        self.dropout_caps = nn.Dropout(0.3)
        self.digit_caps = DigitCaps(dim_caps_in=16, num_caps_out=num_classes,
                                    dim_caps_out=16, num_routing=3)

    def forward(self, x):
        enc = self.encoder(x)
        seg = self.decoder(enc)

        primary_caps = self.primary_caps(enc)
        primary_caps = self.dropout_caps(primary_caps)
        digit_caps = self.digit_caps(primary_caps)
        cls = torch.norm(digit_caps, dim=-1)

        return seg, cls

